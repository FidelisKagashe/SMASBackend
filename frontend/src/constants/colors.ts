// Color utility functions
export const getColorShades = (colorName: keyof typeof ColorPalettes) => {
  return ColorPalettes[colorName];
};

export const getRandomColor = () => {
  const colorNames = Object.keys(ColorPalettes).filter(name => 
    !['brand', 'semantic', 'business'].includes(name)
  );
  const randomColorName = colorNames[Math.floor(Math.random() * colorNames.length)] as keyof typeof ColorPalettes;
  const shades = ColorPalettes[randomColorName];
  const shadeKeys = Object.keys(shades);
  const randomShade = shadeKeys[Math.floor(Math.random() * shadeKeys.length)];
  return (shades as any)[randomShade];
};

export const getContrastColor = (backgroundColor: string) => {
  // Simple contrast calculation
  const hex = backgroundColor.replace('#', '');
  const r = parseInt(hex.substr(0, 2), 16);
  const g = parseInt(hex.substr(2, 2), 16);
  const b = parseInt(hex.substr(4, 2), 16);
  const brightness = (r * 299 + g * 587 + b * 114) / 1000;
  return brightness > 128 ? '#000000' : '#FFFFFF';
};

